public class KonversiSuhu {
    public void celciusToFahrenheit(double a){
        System.out.println("Perubahan suhu Celcius ke Fahrenheit = " + ((a*9.0/5.0)+32) + " F°");
    }
    public void celciusToReamur(double a){
        System.out.println("Perubahan suhu Celcius ke Reamur = " + (4.0/5.0*a) + " R°");
    }
}
